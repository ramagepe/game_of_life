class InvertedGGG(Lifeform):
    def __init__(self) -> None:
        super(InvertedGGG, self).__init__()

    @property
    def layout(self) -> np.ndarray:
        ggg = GGG()
        inv = ggg.layout
        height, width = ggg.size
        for x in range(height):
            for y in range(width):
                inv[x][y], inv[-x][-y] = inv[-x][-y], inv[x][y]
        return inv
